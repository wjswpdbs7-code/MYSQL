import pymysql
#=====================================================================변수 설정=====================================================================

host = "localhost"# mysql이 내 컴퓨터에 설치되어 있기 때문에 localhost
port = 3306 # mysql의 기본 포트 번호는 3306
database = "madang" # mysql에서 사용할 데이터베이스 이름
username = "root" # mysql의 기본 사용자 이름은 root
password = "1234" # mysql의 기본 비밀번호는 1234

conflag = True # DB 연결 성공 여부를 나타내는 플래그 변수

#=====================================================================DB 연결=====================================================================    
try:#DB 연결 시도
    print("DB연결 시작")# DB 연결 시작 메시지 출력
    conn = pymysql.connect(#  뜻 풀이: conn이라는 변수에 pymysql 라이브러리의 connect 함수를 사용하여 DB 연결 정보를 전달하여 연결을 시도하는 코드입니다.
        host = host, user = username, passwd = password, db = database, port = port, charset = "utf8" # DB 연결 정보 전달: host, user, passwd, db, port, charset 등의 정보를 connect 함수에 전달하여 DB 연결을 시도하는 코드입니다.
    )
    print("DB연결 완료") # DB 연결 완료 메시지 출력

#================================================================================DB 연결 실패 예외 처리=====================================================================

except Exception as e: # DB 연결 실패 시 예외 처리: DB 연결 시도 중에 예외가 발생할 경우, 예외 메시지를 출력하고 DB 연결 실패 메시지를 출력하는 코드입니다.
    print("DB연결 실패")# DB 연결 실패 메시지 출력
    conflag = False# DB 연결 실패 플래그 설정: DB 연결이 실패한 경우, conflag 변수를 False로 설정하여 이후 코드에서 DB 연결 여부를 확인할 수 있도록 하는 코드입니다.

#=====================================================================DB 연결 성공 시 실행=====================================================================

if conflag == True:# DB 연결이 성공한 경우에만 실행되는 코드 블록입니다. 
    cursor = conn.cursor()# DB 연결이 성공한 경우, conn 객체에서 cursor() 메서드를 호출하여 cursor 객체를 생성하는 코드입니다. cursor 객체는 DB와의 상호작용을 담당하며, SQL 쿼리를 실행하고 결과를 가져오는 데 사용됩니다.

    q = 'select * from book;'#실행할 SQL 쿼리: book 테이블에서 모든 데이터를 선택하는 SQL 쿼리를 문자열로 정의하는 코드입니다.
    res = cursor.execute(q)# SQL 쿼리 실행: cursor 객체의 execute() 메서드를 사용하여 정의된 SQL 쿼리를 실행하는 코드입니다. 실행 결과로 영향을 받은 행의 수가 res 변수에 저장됩니다.
    data = cursor.fetchall()#다음과 같이 SQL 쿼리 실행 결과를 가져오는 코드입니다. cursor 객체의 fetchall() 메서드를 사용하여 실행된 SQL 쿼리의 결과를 모두 가져와 data 변수에 저장하는 코드입니다.
    cursor.close()# DB 연결 종료: SQL 쿼리 실행이 완료된 후, cursor 객체의 close() 메서드를 호출하여 DB 연결을 종료하는 코드입니다. 이는 DB 리소스를 해제하고 연결을 닫는 역할을 합니다.
    conn.close()# DB 연결 종료: conn 객체의 close() 메서드를 호출하여 DB 연결을 완전히 종료하는 코드입니다. 이는 DB 리소스를 해제하고 연결을 닫는 역할을 합니다.

#=====================================================================SQL 쿼리 결과 출력=====================================================================================
for row in data: 
    print(row[0], row[1], row[2], row[3]) # SQL 쿼리 결과 출력: for 루프를 사용하여 data 변수에 저장된 SQL 쿼리 결과를 반복하면서 각 행의 첫 번째, 두 번째, 세 번째, 네 번째 열의 값을 출력하는 코드입니다. row[0], row[1], row[2], row[3]는 각각 행의 첫 번째, 두 번째, 세 번째, 네 번째 열의 값을 나타냅니다.


#파이썬에서 :는 들여쓰기 블록을 나타냅니다. if, elif, else 등의 조건문이나 for, while 등의 반복문에서 사용됩니다. 들여쓰기 블록은 해당 조건이나 반복이 참일 때 실행되는 코드 블록을 정의하는 역할을 합니다. c나 c++에 서는 중괄호 {}로 코드 블록을 정의하지만, 파이썬에서는 들여쓰기를 사용하여 코드 블록을 구분합니다. 따라서 :는 파이썬에서 코드 블록의 시작을 나타내는 중요한 문법 요소입니다.

