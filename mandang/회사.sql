select staff.ename, staff.job
from emp staff, emp manager
where staff.mgr = manager.empno and manager.ename like 'BLAKE'; -- =와 like

해당 구문을 뜻풀이 하면 
Emp 테이블에서 staff라는 별칭을 가진 행과 manager라는 별칭을 가진 행을 조인하여, staff의 mgr 열이 manager의 empno 열과 일치하고, manager의 ename 열이 'BLAKE'인 행의 staff의 ename 열과 job 열을 조회하는 SQL 쿼리입니다.

즉 Emp 테이블에서 직원과 그 직원의 상사를 조인하여, 상사의 이름이 'BLAKE'인 직원들의 이름과 직업을 조회하는 쿼리입니다.

